<h1>Comment utiliser “Jamais sans ma pomme“ ?</h1>




<h4>1.	L’inscription et l’authentification d’un membre</h4>
<p/>En appuyant sur l’onglet “S’inscrire“, situé sur la barre menu en haut de page, tout utilisateur, pourvu qu’il soit majeur, à la possibilité de devenir client du site. Il peut choisir non seulement de devenir vendeur, acheteur ou bien échangeur. De plus, lorsque son inscrit est terminé, il a la possibilité de se connecter, en appuyant sur l’onglet “Se connecter“. Il pourra ainsi remplir ou vider son panier suivant ses envies et revenir quand bon lui semble.


<h4>2.	La visualisation des fruits et légumes récemment mis sur le site</h4>
<p/>Un utilisateur a la possibilité de visualiser les ajouts récents mis sur le site sur la page d’accueil. Cliquez sur l’onglet “Accueil“. Il a également la possibilité de visualiser les offres proposés par les producteurs en allant directement dans l’onglet “Acheter/Echanger“ ->  “Nos produits“.


<h4>3.	La visualisation détaillée de l’offre et du profil d’un membre</h4>
<p/>Pour visualiser les profils des membres, il suffit de cliquer sur l’onglet « Acheter/Echanger“ -> “Nos producteurs“. Pour visualiser une offre, cliquez sur l’onglet « Acheter/Echanger“ -> “Nos produits“.


<h4>4.	La recherche simple (textuelle) et avancée d’un fruit ou d’un légume</h4>
<p/>La recherche simple d’un fruit ou d’un légume se fait dans l’onglet “Rechercher“. Il est également possible de rechercher un fruit ou un légume directement depuis l’onglet « Acheter/Echanger“ -> “Nos produits“.


<h4>5.	La visualisation des stocks d’une denrée chez un vendeur précis</h4>
<p/>Via l’onglet « Acheter/Echanger“ -> “Nos producteurs“, il est possible non seulement d’accéder au profil d’un producteur précis mais également de voir les offres qu’il propose.


<h4>6.	La proposition d’offres d’achat ou d’échange de denrées</h4>
<p/>Le choix d’achat ou d’échange de denrées se fait lors de l’inscription au site.

 
<h4>7.	L’ajout de nouveaux légumes ou légumes</h4>
<p/>Les membres ont la possibilité d’ajouter de nouveaux fruits ou légumes en allant dans l’onglet “Acheter/Echanger“ -> “Nos produits“ -> “Ajouter un produit“. Il suffit alors de rentrer un nom de produit ainsi qu’un éventuel commentaire et de cliquer sur “Ajout d’article“.


<h4>9.	Un forum de discussion</h4>
<p/>Le forum est accessible via l’onglet “Forum“. Il permet de prodiguer des conseils d’achat ou de culture entre les membres.


<h4>10.	Une aide en ligne</h4>
<p/>La simplicité d’utilisation du site a poussée les fabricants du site à ne pas concevoir d’aide en ligne, à ne rédiger qu’un manuel d’utilisation. Pour toute question, n’hésitez pas à vous référer à l’onglet “Contact“ en bas de page.


